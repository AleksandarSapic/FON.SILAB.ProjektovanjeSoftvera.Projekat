    private void fillTable(List<Athlete> athletes) {
        SearchAthleteTableModel searchAthleteTableModel = new SearchAthleteTableModel(athletes);
        tblAthlete.setModel(searchAthleteTableModel);
    }

    private void loadAllCompetitions() throws Exception {
        Response response = controller.getAllAthletes();
        if (response.getException() != null) {
            throw response.getException();
        }
        showedAthletes = (List<Athlete>) response.getResult();
        if (showedAthletes.isEmpty()) {
            showMessage("Trenutno ne postoji ni jedan unet atletičar.");
            return;
        }
        fillTable(showedAthletes);
    }

    public void updateTable() {
        try {
            loadAllCompetitions();
        } catch (Exception ex) {
            Logger.getLogger(SearchAthleteForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private Athlete makeAnAthlete(int selectedRow) {
        Athlete athlete = new Athlete();

        athlete.setId((int) tblAthlete.getValueAt(selectedRow, 0));
        athlete.setFirstname((String) tblAthlete.getValueAt(selectedRow, 1));
        athlete.setLastname((String) tblAthlete.getValueAt(selectedRow, 2));
        athlete.setBirthday((LocalDate) tblAthlete.getValueAt(selectedRow, 3));
        athlete.setState((String) tblAthlete.getValueAt(selectedRow, 4));

        return athlete;
    }

    private void showMessage(Object message) {
        JOptionPane.showMessageDialog(this, message);
    }

    private void showError(Object errorMessage) {
        JOptionPane.showMessageDialog(this, errorMessage, "Greška", JOptionPane.ERROR_MESSAGE);
    }
