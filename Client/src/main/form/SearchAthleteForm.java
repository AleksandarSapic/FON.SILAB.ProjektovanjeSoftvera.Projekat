    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchParam = txtSearch.getText();
        Response response = controller.searchAthletes(searchParam);
        if (response.getException() != null) {
            if (response.getException() instanceof IOException) {
                showError("Sistem je pao.");
                dispose();
                parent.dispose();
                return;
            }
            showError(response.getException().getMessage());
            dispose();
            return;
        }
        showedAthletes = (List<Athlete>) response.getResult();
        if (showedAthletes.isEmpty()) {
            showError("Sistem ne može da nađe atletičare po zadatoj vrednosti");
        } else {
            showMessage("Sistem je našao atletičare po zadatoj vrednosti");
        }
        fillTable(showedAthletes);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        int selectedRow = tblAthlete.getSelectedRow();
        if (selectedRow == -1) {
            showError("Odaberite atletičara za koga želite da vidite podatke.");
            return;
        }
        Athlete athlete = makeAnAthlete(selectedRow);
        UpdateAthleteForm updateAthleteForm = new UpdateAthleteForm(parent, true, this, controller);
        updateAthleteForm.prepareView(athlete);
        updateAthleteForm.setVisible(true);

    }//GEN-LAST:event_btnDetailsActionPerformed

