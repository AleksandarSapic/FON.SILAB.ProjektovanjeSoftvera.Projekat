    private void prepareView() throws FileNotFoundException, IOException {
        FileReader reader = new FileReader("src/config/app.config");
        Properties p = new Properties();
        p.load(reader);
        String port = p.getProperty("port");
        String user = p.getProperty("user");
        String password = p.getProperty("password");
        String url = p.getProperty("url");

        txtPort.setText(port);
        txtUsername.setText(user);
        if (password.isEmpty()) {
            txtPassword.setText("Prazan string");
        } else {
            txtPassword.setText(password);
        }
        txtUrl.setText(url);

        txtPort.setEditable(false);
        txtUsername.setEditable(false);
        txtPassword.setEditable(false);
        txtUrl.setEditable(false);

        btnSave.setEnabled(false);
    }

    private void updateProperty(String filePath, String propertyName, String propertyValue) throws IOException {
        Properties properties = new Properties();

        FileInputStream inputStream = new FileInputStream(filePath);
        properties.load(inputStream);
        inputStream.close();

        properties.setProperty(propertyName, propertyValue);

        FileOutputStream outputStream = new FileOutputStream(filePath);
        properties.store(outputStream, "Application Configuration");
        outputStream.close();
    }
