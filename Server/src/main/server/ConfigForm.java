    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String port = txtPort.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String url = txtUrl.getText();

        try {
            updateProperty("src/config/app.config", "port", port);
            updateProperty("src/config/app.config", "user", username);
            updateProperty("src/config/app.config", "password", password);
            updateProperty("src/config/app.config", "url", url);
            JOptionPane.showMessageDialog(this, "Novi konfiguracioni parametri su uspešno sačuvani.");
            dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }

        txtPort.setEditable(false);
        txtUsername.setEditable(false);
        txtPassword.setEditable(false);
        txtUrl.setEditable(false);
        btnSave.setEnabled(false);
        btnChange.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

